// ─── Time-to-hire report ──────────────────────────────────────────────────────

export function downloadTimeToHireReport(apps: Application[], jobs: Job[], actor: string) {
  const doc = new jsPDF();
  header(doc, "Time-to-Hire Report", `Days from application date to current status — ${new Date().toLocaleDateString()}`);

  const jobMap = new Map(jobs.map((j) => [j.id, j]));
  const byJob: Record<number, { title: string; count: number; totalDays: number; offered: number }> = {};

  apps.forEach((a) => {
    if (!a.jobId) return;
    const d = new Date(a.date);
    if (Number.isNaN(d.getTime())) return;
    const days = Math.round((Date.now() - d.getTime()) / 86_400_000);
    if (!byJob[a.jobId]) byJob[a.jobId] = { title: jobMap.get(a.jobId)?.title ?? `Job #${a.jobId}`, count: 0, totalDays: 0, offered: 0 };
    byJob[a.jobId].count++;
    byJob[a.jobId].totalDays += days;
    if (a.status === "Offered") byJob[a.jobId].offered++;
  });

  const rows = Object.entries(byJob).map(([, v]) => [
    v.title,
    v.count,
    v.offered,
    v.count > 0 ? `${Math.round(v.totalDays / v.count)} days` : "—",
    v.offered > 0 ? `${Math.round(v.totalDays / v.offered)} days` : "—",
  ]);

  autoTable(doc, {
    startY: 65,
    head: [["Job Title", "Applications", "Offers Made", "Avg Days (all)", "Avg Days (to offer)"]],
    body: rows,
    headStyles: { fillColor: NAVY, textColor: 255, fontStyle: "bold" },
    styles: { fontSize: 8.5, cellPadding: 2.5 },
    alternateRowStyles: { fillColor: [245, 247, 250] },
  });

  doc.setFontSize(7.5);
  doc.setTextColor(100, 100, 100);
  doc.text("Note: 'Avg Days (all)' counts from application date to today. 'Avg Days (to offer)' applies to offered candidates only.", 10, (doc as any).lastAutoTable.finalY + 6, { maxWidth: 190 });
  footer(doc, actor);
  doc.save(`caa-time-to-hire-${Date.now()}.pdf`);
}

// ─── Diversity summary ────────────────────────────────────────────────────────

