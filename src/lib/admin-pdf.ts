// ─── Applicants per closing date ──────────────────────────────────────────────

export function downloadApplicantsPerClosingDateReport(
  apps: Application[], jobs: Job[], actor: string,
  fromDate?: string, toDate?: string,
) {
  const doc = new jsPDF();
  const rangeLabel = fromDate || toDate
    ? ` (${fromDate || "start"} – ${toDate || "now"})`
    : "";
  header(doc, "Applicants per Closing Date", `Vacancy demand by deadline${rangeLabel} — ${new Date().toLocaleDateString()}`);

  const filtered = apps.filter((a) => {
    if (!fromDate && !toDate) return true;
    const d = new Date(a.date);
    if (fromDate && d < new Date(fromDate)) return false;
    if (toDate && d > new Date(toDate)) return false;
    return true;
  });

  const jobMap = new Map(jobs.map((j) => [j.id, j]));
  const byJob: Record<number, { title: string; closes: string; count: number }> = {};
  filtered.forEach((a) => {
    if (!a.jobId) return;
    const j = jobMap.get(a.jobId);
    if (!byJob[a.jobId]) byJob[a.jobId] = { title: j?.title ?? `Job #${a.jobId}`, closes: j?.closes ?? "—", count: 0 };
    byJob[a.jobId].count++;
  });

  const rows = Object.values(byJob)
    .sort((a, b) => b.count - a.count)
    .map((v) => [v.title, v.closes, v.count]);

  autoTable(doc, {
    startY: 65,
    head: [["Job Title", "Closing Date", "Applications Received"]],
    body: rows,
    headStyles: { fillColor: NAVY, textColor: 255, fontStyle: "bold" },
    styles: { fontSize: 8.5, cellPadding: 2.5 },
    alternateRowStyles: { fillColor: [245, 247, 250] },
  });

  footer(doc, actor);
  doc.save(`caa-applicants-per-closing-date-${Date.now()}.pdf`);
}