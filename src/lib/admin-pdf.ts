const NAVY: [number, number, number] = [13, 36, 84];
const ACCENT: [number, number, number] = [201, 162, 39];

function header(doc: jsPDF, title: string, subtitle?: string) {
  // White background
  doc.setFillColor(255, 255, 255);
  doc.rect(0, 0, 210, 297, "F");

  // Logo (left side)
  try { doc.addImage(CAA_LOGO_BASE64, "JPEG", 10, 8, 22, 22); } catch {}

  // Organisation name (right of logo, vertically centered)
  doc.setTextColor(...NAVY);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(16);
  doc.text("UGANDA CIVIL AVIATION AUTHORITY", 36, 17);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8);
  doc.setTextColor(80, 80, 80);
  doc.text("Entebbe International Airport  |  P.O. Box 5536, Kampala, Uganda", 36, 23);
  doc.text("Tel: +256 312 352 000  |  aviation@caa.go.ug  |  www.caa.go.ug", 36, 28);

  // Blue separator line (matching the letter's blue rule)
  doc.setDrawColor(...NAVY);
  doc.setLineWidth(0.8);
  doc.line(10, 33, 200, 33);
  doc.setFillColor(...ACCENT);
  doc.rect(10, 33.8, 190, 0.4, "F");

  // Reference and date (like a formal letter)
  doc.setTextColor(60, 60, 60);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8);
  doc.text("Our Ref: UCAA/HR/PORTAL", 10, 40);
  doc.text(new Date().toLocaleDateString("en-GB", { day: "numeric", month: "long", year: "numeric" }), 200, 40, { align: "right" });

  // Document title
  doc.setFont("helvetica", "bold");
  doc.setFontSize(13);
  doc.setTextColor(...NAVY);
  doc.text(title.toUpperCase(), 10, 50);
  doc.setDrawColor(...NAVY);
  doc.setLineWidth(0.3);
  doc.line(10, 52, Math.min(10 + doc.getTextWidth(title.toUpperCase()), 200), 52);

  if (subtitle) {
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    doc.setTextColor(60, 60, 60);
    doc.text(subtitle, 10, 58);
  }
}

function footer(doc: jsPDF, actor: string) {
  const pages = doc.getNumberOfPages();
  for (let i = 1; i <= pages; i++) {
    doc.setPage(i);
    // Footer rule
    doc.setDrawColor(...NAVY);
    doc.setLineWidth(0.5);
    doc.line(10, 283, 200, 283);
    doc.setFontSize(7.5);
    doc.setTextColor(80, 80, 80);
    doc.setFont("helvetica", "normal");
    doc.text("Uganda Civil Aviation Authority — Confidential HR Document. Not for external distribution.", 10, 287);
    doc.text(`Generated by ${actor}`, 10, 291);
    doc.text(`Page ${i} of ${pages}`, 200, 291, { align: "right" });
  }
}

